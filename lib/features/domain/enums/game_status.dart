enum GameStatus {
  /// Игра еще не началась (экран загрузки или начальный экран уровня).
  notStarted,

  /// Игрок совершает ходы.
  inProgress,

  /// Все цвета собраны в отдельные колбы.
  /// Условие: каждая непустая колба содержит только один цвет и имеет свободное место сверху (или является полностью заполненной этим цветом).
  won,

  /// Закончились доступные ходы, но на поле остались перемешанные цвета.
  /// В классической версии Water Sort это состояние достигается редко,
  /// обычно оно означает использование кастомных правил (например, отсутствие пустых колб-буферов).
  failed,
}
